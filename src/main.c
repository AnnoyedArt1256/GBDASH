void waitforframes(char frames) {
    while (frames > 0) {
        frames--;
        wait_vbl_done();
    }
}

void main(void)
{
    register_all_music();
    DISPLAY_OFF;
    set_bkg_data(0,44,Logo);
    set_bkg_tiles(0,0,32,32,blank);
    set_bkg_tiles(2,2,16,3,logo);

    scroll_bkg(0,64);
    SHOW_BKG;
    play(0);

    waitforframes(20);
    BGP_REG = 0;
    DISPLAY_ON;
    waitforframes(5);
    BGP_REG = 0b01010101;
    waitforframes(5);
    BGP_REG = 0b10101010;
    waitforframes(5);
    BGP_REG = 0b11111111;
    waitforframes(5);
    BGP_REG = 0b11101010;
    waitforframes(5);
    BGP_REG = 0b11100101;
    waitforframes(5);
    BGP_REG = 0b11100100;
    char sc = 0;

    while (SCY_REG > 0)
    {
        sc++;
        SCY_REG -= sc;
        if ((char)SCY_REG < 0) {
            sc *= -1;
            sc += 2;
            SCY_REG = 1;
        }
        wait_vbl_done();
    }

}